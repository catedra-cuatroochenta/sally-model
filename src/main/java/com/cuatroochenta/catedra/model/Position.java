package com.cuatroochenta.catedra.model;

public class Position {
    private float X;
    private float Y;
    private float Z;

    public Position() {
    }

    public float getX() {
        return X;
    }

    public void setX(float x) {
        X = x;
    }

    public float getY() {
        return Y;
    }

    public void setY(float y) {
        Y = y;
    }

    public float getZ() {
        return Z;
    }

    public void setZ(float z) {
        Z = z;
    }
}
